import java.util.*;

class Solution {
    public long solution(int n, int[] times) {
        Arrays.sort(times);
        
        long left = 1; // 최소 시간
        long right = (long) times[times.length - 1] * n; // 최대 시간
        long answer = right;
        
        while (left <= right) {
            long mid = (left + right) / 2;
            long total = 0;
            
            // mid 시간 동안 처리 가능한 사람 수 계산
            for (int time : times) {
                total += mid / time;
                if (total >= n) break; // 이미 충분하면 종료
            }
            
            if (total >= n) {
                // n명 이상 처리 가능 → 시간 줄여보기
                answer = mid;
                right = mid - 1;
            } else {
                // n명 처리 불가능 → 시간 늘려보기
                left = mid + 1;
            }
        }
        
        return answer;
    }
}